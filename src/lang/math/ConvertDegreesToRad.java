package lang.math;

public class ConvertDegreesToRad {
    public static void main(String[] args) {
        double degrees = 50;
        double radiana = 50;

        System.out.println(degrees+" of degrees, "+Math.toRadians(degrees)+" a radians.");
        System.out.println(radiana+" of radians, "+Math.toDegrees(radiana)+" a degrees.");
    }
}
